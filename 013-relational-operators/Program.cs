using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _013_relational_operators
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("5 == 10: " + (5 == 10));
            Console.WriteLine("5 != 10: " + (5 != 10));
            Console.WriteLine("5 < 10: " + (5 < 10));
            Console.WriteLine("5 > 10: " + (5 > 10));
            Console.WriteLine("5 <= 10: " + (5 <= 10));
            Console.WriteLine("5 >= 10: " + (5 >= 10));

            Console.ReadKey();
        }
    }
}
