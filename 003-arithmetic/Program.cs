using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _003_arithmetic
{
    class Program
    {
        static void Main(string[] args)
        {
            // Addition
            Console.WriteLine("7 + 5 = " + (7 + 5));

            // Subtraction
            Console.WriteLine("7 - 5 = " + (7 - 5));

            // Multiplication
            Console.WriteLine("7 * 5 = " + (7 * 5));

            // Division
            Console.WriteLine("7 / 5 = " + (7 / 5));

            // Modulus (remainder of division)
            Console.WriteLine("7 % 5 = " + (7 % 5));

            // Unary Negation
            Console.WriteLine("-(-5) = " + (-(-5)));

            Console.ReadKey();
        }
    }
}
